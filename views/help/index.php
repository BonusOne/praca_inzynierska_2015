<b>1. Czy można przewozić wózek inwalidzki?</b><br />
<div class="faq-odp">Przewóz wózków inwalidzkich jest dozwolony i bezpłatny.</div>
<br />


<b>2. Czy można przewozić instrumenty muzyczne?</b><br />
<div class="faq-odp">Przewoźnik umożliwia bezpłatny przewóz instrumentów muzycznych pod warunkiem dostępnego miejsca w luku bagażowym oraz odpowiedniego ich zabezpieczenia (przewóz w futerałach ochronnych).</div>
<br />


<b>3. Czy można zabrać laptop na pokład autokaru?</b>
<div class="faq-odp">Tak, można zabrać ze sobą laptop na pokład autokaru.</div>
<br />


<b>4. Jak otrzymać fakturę VAT?</b>
<div class="faq-odp">Zgodnie z rozporządzeniem Ministra Finansów z dnia 28 marca 2011 r. e-Bilet jest uznany za fakturę VAT. W przypadku chęci otrzymania imiennej faktury VAT prosimy o przesłanie maila na adres <a href="mailto:info@pawelbus.pl">info@pawelbus.pl</a> wpisując w temacie faktura VAT, w treści maila prosimy o podanie danych do faktury oraz danych adresowych.</div>

